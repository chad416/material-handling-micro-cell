def nodes_by_id() -> dict[str, NodeSpec]:
    return {spec.node_id: spec for spec in NODE_SPECS}


def specs_for_category(category: str) -> tuple[NodeSpec, ...]:
    return tuple(spec for spec in NODE_SPECS if spec.category == category)


