"""Semantic OPC UA information model for MHMC-01.

The PLC should stay free of hard-coded transport details.  This module is the
shared contract between the OPC UA runtime, SCADA collector configuration, and
the IEC 61131-3 symbolic variables exposed by ``Main``.
"""

from __future__ import annotations

from dataclasses import dataclass
from enum import Enum
from typing import Any, Iterable


NAMESPACE_URI = "http://antigravity.automation.org/MHMC/"
APPLICATION_URI = "urn:antigravity:automation:mhmc:opcua-server"


class NodeAccess(str, Enum):
    READ_ONLY = "RO"
    READ_WRITE = "RW"


@dataclass(frozen=True)
class NodeSpec:
    """One semantic variable in the MHMC OPC UA namespace."""

    node_id: str
    browse_name: str
    path: tuple[str, ...]
    variant_type: str
    default: Any
    access: NodeAccess
    description: str
    plc_symbol: str
    category: str
    historian_measurement: str
    historian_field: str
    engineering_unit: str = ""

    @property
    def is_writable(self) -> bool:
        return self.access == NodeAccess.READ_WRITE


